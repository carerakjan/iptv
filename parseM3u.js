function convertInto2KOM(m3u) {
    return m3u
        .replace('#EXTM3U', '')
        .split('#EXTINF:0,')
        .slice(1)
        .map(function(str, index) {
            var channel = str.split('\n').slice(0,-1);

            return {
                "id": index + 1,
                "number": index + 1,
                "caption": channel[0],
                "icon_url": "",
                "tv_categories": [2],
                "streaming_url": channel[1],
                "announce": "",
                "volume_shift": 0
            };
        });
}